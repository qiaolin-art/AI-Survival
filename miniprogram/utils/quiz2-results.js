function getTier(probability) {
  for (var i = 0; i < TIERS.length; i++) {
    if (probability >= TIERS[i].min && probability <= TIERS[i].max) {
      return TIERS[i];
    }
  }
  return TIERS[TIERS.length - 1];
}

function getRole(J, D, V, I) {
  var key = (J >= 0 ? 'J+' : 'J-') +
            (D >= 0 ? 'D+' : 'D-') +
            (V >= 0 ? 'V+' : 'V-') +
            (I >= 0 ? 'I+' : 'I-');
  return ROLES[key] || ROLES['J-D-V-I-'];
}

module.exports = { TIERS: TIERS, ROLES: ROLES, getTier: getTier, getRole: getRole };
