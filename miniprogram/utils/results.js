function getTier(probability) {
  for (var i = 0; i < TIERS.length; i++) {
    if (probability >= TIERS[i].min && probability <= TIERS[i].max) {
      return TIERS[i];
    }
  }
  return TIERS[TIERS.length - 1];
}

function getRole(R, S, F, A) {
  var key = (R >= 0 ? 'R+' : 'R-') +
            (S >= 0 ? 'S+' : 'S-') +
            (F >= 0 ? 'F+' : 'F-') +
            (A >= 0 ? 'A+' : 'A-');
  return ROLES[key] || ROLES['R-S-F-A-'];
}

